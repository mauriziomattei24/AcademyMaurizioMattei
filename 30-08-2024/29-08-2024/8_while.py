# L'istruzione while viene utilizzata per eseguire un blocco di codice finché una determinata condizione è vera. 
# Prima di ogni iterazione, la condizione viene verificata e se è vera, il blocco di codice viene eseguito. 
# Ecco un esempio
# while serve quando non sappiamo il numero di iterazioni


conteggio = 0
while conteggio < 5:
    print(conteggio)
    conteggio += 1